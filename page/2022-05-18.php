	<div class="block block-pink"><!--原文翻译-->
		<h3 style="text-align: center">原文翻译</h3>
		<br/>君子说，学习不可以停止。靛青是从蓼蓝中提取的，但它比蓼蓝的颜色更青；冰是由水凝结成的，但它比水更冷。木材直得合乎拉直的墨绳，如果给它加热使它弯曲做成车轮，它的弯度就可以合乎圆规，即使又晒干了，也不会再挺直，这是因为人工使它弯曲成这样。所以木材经墨线量过就笔直了，金属刀具在磨刀石上磨过就锋利了。君子广泛地学习而且每天对照检查自己，就智慧明达，行动不会犯错误了。
		<br/>我曾经整日思索，却不如学习片刻收获大；我曾经踮起脚跟眺望，却不如登上高处看得广阔。登高招手，手臂并没有加长，但人们在远处也能看见；顺着风呼喊，声音并没有加大，但听的人会听得清楚；借助车马的`人，不是脚走得快，却能到达千里之外；借助船只的人，不是自己能游泳，却能横渡江河。君子的本性同一般人没有差别，只是善于借助于外物罢了。
		<br/>堆土成山，风雨就从那里兴起；水流汇成深渊，蛟龙就在那里生长；积累善行，形成良好的品德，就会得到最高的智慧，具备圣人的思想境界。所以，不积累每一小步，就不能远达千里；不汇集细流，就不能形成江海。骏马跳跃一次，不能有十步远；劣马拉车走十天，也能走得很远，它的成功在于不停止。拿刀刻东西，中途停止，腐朽的木头也不能刻断；不停地刻下去，金石也能雕刻成功。蚯蚓没有锋利的爪牙，坚强的筋骨，却能上吃泥土，下饮泉水，是因为它心专一；螃蟹有六条腿，两只大夹，然而没有蛇鳝的洞穴就无处容身，是因为它心浮躁的缘故。
