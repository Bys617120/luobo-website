	<h3 style="text-align: center">烛之武退秦师</h3>
		
		<p>
			1. 《左传》，是我国第一部叙事详细的编年史著作，相传为春秋末年鲁国史官左丘明所作。他依孔子修订的鲁史的顺序，主要记载了东周前期二百四五十年间各国政治、经济、军事、外交和文化方面的重要事件和重要人物，是研究我国先秦历史很有价值的文献，也是优秀的散文著作。
		</p>
		
		<p>
			2.儒家十三经
		儒家十三经，是指封建统治者先后将13部儒家书籍“法定”为“经”，形成了封建社会具有特殊地位的“十三经”。分别是《诗经》、《尚书》、《周礼》、《仪礼》、《礼记》、《周易》、《左传》、《公羊传》、《谷梁传》、《论语》、《尔雅》、《孝经》、《孟子》。
		</p>
		<p>
		3.晋侯、秦伯围郑,以其无礼于晋，且贰于楚也。
		此句里的“晋侯”“秦伯”分别指晋文公和秦穆公，其中“侯”“伯”是古代爵位名称。旧说周代有公、侯、伯、子、男五种爵位，爵位、爵号，是古代皇帝对贵戚功臣的封赐，后代爵称和爵位制度往往因时而异。
		</p>
		
		<p>
		4.公曰：“吾不能早用子，今急而求子，是寡人之过也”。
		（1）句中的“子”，指代烛之武。子，是古代对男子的尊称，相当于现代汉语的“您”。有时写在姓氏后面，作为对人的尊称，如“孔子”“荀子”“庄子”等。
		（2）句中的“寡人”，指代郑伯。寡人，即为寡德之人，意为“在道德方面做得不足的人”。是古代君主、诸侯王对自己的谦称。中国古代讲究“以德治国”，“以德配天”，就是说君主、诸侯王的权位是上天赋予的，但上天只会把天下给有德的人，君主、诸侯王如果失德就会失去尊贵的权位，所以君主、诸侯王就谦称自己是“寡人”。
		</p>
