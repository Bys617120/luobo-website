<div class="block block-purple">
	<h3 style="text-align: center">荆轲刺秦王</h3>
	<p>
	1.《战国策》
	《战国策》，国别体史书，又称《国策》。主要记述了战国时期的游说之士的政治主张和言行策略，也可说是游说之士的实战演习手册。《战国策》一书的思想倾向,因其与儒家正统思想相悖,受到历代学者的贬斥。作者并非一人，成书并非一时，书中文章作者大多不知是谁。西汉刘向编定为三十三篇，书名亦为刘向所拟定。
	</p>
	<p>
	2.秦兵旦暮渡易水，则虽欲常侍足下，岂可得哉？
	足下,是旧时汉族交际用语，下称上或同辈相称的敬词。战国时多用以称君主。
	</p>
	<p>
	3.今日往而不反者，竖子也！
	竖子：①童仆   ②小子，对人的蔑称
	</p>
	<p>
	4.高渐离击筑，荆轲和而歌，为变徵之声，士皆垂泪涕泣。
	变徵，是徵音的变调，声调悲凉。古代音乐分为宫、商、角、徵、羽、变宫、变徵七音。
	</p>
	<p>
	5.愿举国为内臣，比诸侯之列，给贡职如郡县，而得奉守先王之宗庙。
	宗庙,是我国的宗庙制度是祖先崇拜的产物。人们在阳间为亡灵建立的寄居所即宗庙。帝王的宗庙制是天子七庙，诸侯五庙，大夫三庙，士一庙。庶人不准设庙。同时宗庙是供奉历朝历代国王牌位、举行祭祀的地方。
	</p>
	<p>
	6.乃朝服，设九宾，见燕使者咸阳宫。
	九宾，宾，同“傧”。九宾是我国古代外交上最为隆重的礼节，有九个迎宾赞礼的官员司仪施礼，并延引上殿。
	</p>
	<p>
	7.至陛下，秦武阳色变振恐，群臣怪之。
	陛下,“陛”指帝王宫殿的台阶。“陛下”原来指的是站在台阶下的侍者。臣子向天子进言时，不能直呼天子，必须先呼台下的侍者而告之。后来“陛下”就成为与帝王面对面应对的敬称。
	</p>
	<p>
	8.北蛮夷之鄙人，未尝见天子，故振慑，愿大王少假借之，使毕使于前。
	蛮夷，古代泛指华夏民族以外的其他民族，包括南蛮和东夷。多数情况下，蛮夷戎狄统称蛮夷或四夷。《礼记·王制》：“东曰夷、西曰戎、南曰蛮、北曰狄”。
	</p>
	<p>
	9.诸郎中执兵，皆陈殿下，非有诏不得上。
	郎中：①官名。始置于战国，职掌侍卫。秦汉沿置，内充侍卫，外从作战。晋至南北朝，为尚书曹司的长官，隋唐以后，六部都设置郎中，分掌部内各司政务。②宋代以后对医生的称呼。
	</p>
	<p>
	10.荆自知事不就，倚柱而笑，箕踞以骂曰。
	箕踞，两脚张开，两膝微曲地坐着，形状像箕。这是一种不拘礼节的坐法，比喻轻慢傲视对方的姿态。
	</p>
