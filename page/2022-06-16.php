<div class="block">
	<h3 style="text-align: center">鸿门宴</h3>
	<p>
	1.楚左尹项伯者，项羽季父也，素善留侯张良。
	季父，父亲的幼弟。古代以伯、仲、叔、季来表示兄弟间的排行顺序，伯为老大，仲为老二，叔为老三，季排行最小。
	</p>
	<p>2.沛公奉卮酒为寿，约为婚姻。
	（1）为寿，席间敬酒祝健康。
	（2）婚姻，古时又称“昏姻”或“昏因”。婚姻：一是男娶女嫁结为夫妻这件事情；二是指结婚男女双方的父母。《说文》: “妇家为婚，婿家为姻。”婚是女方的父母,姻是男方的父母。“约为婚姻”即“结为儿女亲家”。在我国古代的婚礼中，男方通常在黄昏时到女家迎亲，而女方随着男方出门。这种“男以昏时迎女，女因男而来”的习俗，就是“昏因”一词的起源。
	</p>
	<p>
	【链接】婚姻六礼
	六礼，汉族婚姻仪礼。指从议婚至完婚过程中的六种礼节，即：纳采、问名、纳吉、纳征、请期、亲迎。
	纳彩即男方家请媒人去女方家提亲，女方家答应议婚后，男方家备礼前去求婚。
	问名，即男方家请媒人问女方的名字和出生年月日。
	纳吉，即男方将女子的名字、八字取回后，在祖庙进行占卜。
	纳征，亦称纳币，即男方家以聘礼送给女方家。
	请期，男家择定婚期，备礼告知女方家，求其同意。
	亲迎，即新郎亲至女家迎娶。
	</p>
	<p>
	3.项王、项伯东向坐；亚父，南向坐，——亚父者，范增也；沛公北向坐；张良西向侍。
	古代建筑通常是堂室结构，前堂后室。在堂上举行的礼节活动是南向为尊。皇帝聚会群臣，他的座位一定是坐北向南的。因此，古人常把称王称帝叫做“南面”，称臣叫做“北面”。
	室东西长而南北窄，因此室内最尊的座次是坐西面东，其次是坐北向南，再次是坐南面北，最卑是坐东面西。《鸿门宴》中有这样几句：“项王、项伯东向坐，亚父南向坐，……沛公北向坐，张良西向侍。”项王座次最尊，张良座次最卑。
	</p>
	<p>
	4.张良曰：“沛公之参乘樊哙者也。”
	参乘,亦作“骖乘”：①陪乘。古代乘车之法，尊者居左，驭者居中，陪乘者居右，负责行车安全。战时称“车右”，平时称“骖乘”。②指陪乘的人。
	</p>
	<p>
	5.谨使臣良奉白璧一双，再拜献大王足下，玉斗一双，再拜奉大将军足下。
	再拜：①拜两次。古人表示恭敬的礼节。②敬辞。用于书信的开头或结尾。
	</p>
